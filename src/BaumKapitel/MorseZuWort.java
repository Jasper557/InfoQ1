package BaumKapitel;

import BaumKapitel.baum.BinaryTree;

public class MorseZuWort {
    private final BinaryTree<String> root;

    public MorseZuWort(Morsebaum morsebaum) {
        this.root = morsebaum.getRoot();
    }

    public String MorsezeichenZuWort(String morsecode) {
        StringBuilder text = new StringBuilder();  // -_-
        BinaryTree<String> currentNode = root;

        for (char zeichen : morsecode.toCharArray()) {
            if (zeichen == '.') {
                currentNode = currentNode.getLeftTree();
            } else if (zeichen == '-') {
                currentNode = currentNode.getRightTree();
            } else if (zeichen == '/') {
                text.append(currentNode.getContent());
                currentNode = root;
            }
        }

        text.append(currentNode.getContent());

        return text.toString();
    }

    public static void main(String[] args) {
        Morsebaum morsebaum = new Morsebaum();
        MorseZuWort morseZuWort = new MorseZuWort(morsebaum);

        String morsecode = ".-./.-/-.-./..../--/.-/-./../-./---/..-./..-.";
        String text = morseZuWort.MorsezeichenZuWort(morsecode);

        System.out.println("Morsecode: " + morsecode);
        System.out.println("Klartext: " + text);
    }
}


